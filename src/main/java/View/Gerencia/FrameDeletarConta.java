    public void resetCliente() {
        nomeCliente.removeAllItems();
        nomeCliente.addItem("- Selecione -");
        nomeCliente.setSelectedIndex(0);
    }

    public void resetConta() {
        try {
            cmbConta.removeAllItems();
        } catch (Exception e) {
        }

        cmbConta.addItem("- Selecione -");
        cmbConta.setSelectedIndex(0);
    }

    public void resetStatus() {
        status.setText("---");
    }

    public void preencherNomesClientes() {
        // Limpa os campos
        ControlDeletarConta.limparCampos(this);

        // Preenche com os nomes dos clientes
        ClienteDAO.getInstance().getEntity().clear();
        clientes = ClienteDAO.getInstance().findAll();

        for (Cliente cliente : clientes) {
            nomeCliente.addItem(cliente.getNome());
        }
    }

