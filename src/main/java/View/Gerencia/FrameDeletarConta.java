    private void nomeClienteFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nomeClienteFocusLost
        // Obtém o nome do cliente
        int indexCliente = nomeCliente.getSelectedIndex();
        
        if (indexCliente > 0) {
        
            Cliente cliente = clientes.get(indexCliente - 1);
            resetConta();
            resetStatus();

            for (Conta c : cliente.getConta()) {
                 cmbConta.addItem("AG: " + c.getAgencia() + " CONTA N: " + c.getNumeroConta());
            }
            
        }
    }//GEN-LAST:event_nomeClienteFocusLost

