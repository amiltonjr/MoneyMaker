    private void cmbContaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmbContaMouseReleased
        // Obtém a conta selecionada
        int indexCliente = nomeCliente.getSelectedIndex();
        int indexConta = cmbConta.getSelectedIndex();

        if (indexConta > 0) {
            resetStatus();
            
            Conta conta = clientes.get(indexCliente - 1).getConta().get(indexConta - 1);

            // Obtém o status
            String s = conta.getStatus();

            status.setText(s.toUpperCase());
            
            deletar.setEnabled(true);
        }
    }//GEN-LAST:event_cmbContaMouseReleased

