    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        
         int indexCliente = nomeCliente.getSelectedIndex();
        int indexConta = cmbConta.getSelectedIndex();
        
        Conta conta = clientes.get(indexCliente - 1).getConta().get(indexConta - 1);
        
        boolean status = ControlDeletarConta.deletar(conta, deletar.getText());
        
        if (status) {
            ControlDeletarConta.sucesso();

            ControlDeletarConta.cancelar(this, getParentFrame());
        } else {
            ControlDeletarConta.erro();
        }
    }//GEN-LAST:event_deletarActionPerformed

