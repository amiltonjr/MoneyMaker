    private void contaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_contaFocusLost
        // Obtém a conta selecionada
        int indexCliente = nomeCliente.getSelectedIndex();
        int indexConta = conta.getSelectedIndex();

        if (indexConta > 0) {
            resetLancamento();

            // Obtém os lançamentos
            List<Movimentacao> lancs = clientes.get(indexCliente - 1).getConta().get(indexConta - 1).getMovimentacoes();
            // Adiciona os itens no campo Lançamentos
            for (Movimentacao lanc : lancs) {
                lancamento.addItem(lanc.getOperacao() + " N: " + lanc.getNumero() + " Valor: " + lanc.getValor());
            }
        }
    }//GEN-LAST:event_contaFocusLost

