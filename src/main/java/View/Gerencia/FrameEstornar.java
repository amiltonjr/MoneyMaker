    private void estornarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estornarActionPerformed

        int indexCliente = nomeCliente.getSelectedIndex();
        int indexConta = conta.getSelectedIndex();
        int indexMovimentacao = lancamento.getSelectedIndex();

        try {
            Movimentacao mov = clientes.get(indexCliente - 1).getConta().get(indexConta - 1).getMovimentacoes().get(indexMovimentacao - 1);

            boolean status = ControlEstornar.estornar(mov);

            if (status) {
                ControlEstornar.cancelar(this, getParentFrame());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Erro ao processar a transação!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_estornarActionPerformed

