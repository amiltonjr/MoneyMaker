    private void nomeClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_nomeClienteItemStateChanged

        int indexCliente = nomeCliente.getSelectedIndex();

        if (indexCliente > 0) {
            resetConta();

            Cliente cliente = clientes.get(indexCliente - 1);

            try {
                for (Conta c : cliente.getConta()) {
                    conta.addItem("AG: " + c.getAgencia() + " CONTA N: " + c.getNumeroConta());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(new JFrame(), "Este cliente não possui nenhuma conta!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            }

        }
    }//GEN-LAST:event_nomeClienteItemStateChanged

